class Solution {
    // 1 3 5 6 7 -4 -3 -3 -2 4 6 7
    public int subarraySum(int[] nums, int k) {
        int ans = 0, n = nums.length;
        // <å‰ç¼€å’Œ, ä¸ªæ•°>
        Map<Integer, Integer> map = new HashMap<>();
        int pre = 0;
        map.put(0, 1);
        for (int i = 0; i < n; i++) {
            pre += nums[i];
            if (map.containsKey(pre - k)) {
                ans += map.get(pre - k);
            }
            map.put(pre, map.getOrDefault(pre, 0) + 1);
        }
        return ans;
    }
}
